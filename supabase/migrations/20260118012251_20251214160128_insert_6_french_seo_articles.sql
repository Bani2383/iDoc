<p>Pour prouver votre adresse lorsque vous n''avez pas de facture à votre nom (logement chez un proche, colocation, etc.).</p>

<h3>2. Déclaration de perte de documents</h3>
<p>En cas de perte de passeport, carte d''identité, permis de conduire ou autres documents officiels.</p>

<h3>3. Déclaration de concubinage ou d''union de fait</h3>
<p>Pour les démarches administratives nécessitant la preuve d''une relation (immigration, fiscalité, etc.).</p>

<h3>4. Déclaration de revenus ou de situation financière</h3>
<p>Dans certains contextes (demande de bourse, aide sociale, immigration), une déclaration personnelle peut être exigée.</p>

<h3>5. Déclaration sur l''honneur</h3>
<p>Pour certifier l''exactitude d''informations fournies dans un dossier administratif.</p>

<h2>Structure d''une lettre de déclaration</h2>

